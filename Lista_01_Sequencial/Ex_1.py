"""
Exercício 1: Contagem de Faltas
Escreva um programa que solicite ao usuário a quantidade de faltas do primeiro semestre 
e a quantidade de faltas do segundo semestre. O programa deve calcular e exibir 
na tela a soma total de faltas do aluno no ano.
"""

faltas_1 = int(input("Digite as faltas do primeiro semestre: "))
faltas_2 = int(input("Digite as faltas do segundo semestre: "))
soma_faltas = faltas_1 + faltas_2
print("A soma das faltas do aluno é:", soma_faltas)