"""Criar um programa que peça ao usuário para digitar três notas (ex: 8.5, 7.0 e 9.2).
O programa deve calcular a soma dessas três notas e exibir o resultado final na tela.
"""

nota_1 = float(input("Digite a nota 1: "))
nota_2 = float(input("Digite a nota 2: "))
nota_3 = float(input("Digite a nota 3: "))
soma_notas = nota_1 + nota_2 + nota_3
print("A soma das notas do aluno é:", soma_notas)