"""Desenvolva um programa que solicite três notas ao usuário, 
calcule a média aritmética dessas três notas e exiba o resultado na tela.
"""

nota_1 = float(input("Digite a nota 1: "))
nota_2 = float(input("Digite a nota 2: "))
nota_3 = float(input("Digite a nota 3: "))
media_notas = (nota_1 + nota_2 + nota_3)/3
print("A média das notas é: ", media_notas)