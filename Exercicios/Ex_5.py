"""Escreva um programa que peça ao usuário para digitar uma temperatura em graus Celsius. 
O programa deve converter essa temperatura para graus Fahrenheit e 
exibir o resultado na tela."""

temp_celsius = float(input("Digite a temperatura em graus Celsius: "))
print("Essa temperatura é igual a ", (temp_celsius * 1.8) + 32, " graus Fahrenheit")